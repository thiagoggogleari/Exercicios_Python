# Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

medida = float(input('Insira a medida de um dos lados do quadrado (cm): '))


# A área do quadrado é o produto entre os dois lados.
print('A área do quadrado é de %.2fcm²'%(medida * 2))

print('O dobro desta área apresentada é de %.2fcm²'%(medida * 4))
